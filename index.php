$(document).ready(function() {
    let current = 0; // Start from the first element (0-indexed)
    const $texts = $('.rounded_text .text'); // Get all text elements
    const numberDivs = $texts.length; // Number of text elements

    function showNextText() {
        // Remove active class from the current text
        $texts.eq(current).removeClass('active');
        
        // Increment current index
        current = (current + 1) % numberDivs; // Loop back to 0 if it exceeds

        // Set the next text to be active
        $texts.eq(current).addClass('active');

        // Add slide-out animation for the previous text
        $texts.eq((current - 1 + numberDivs) % numberDivs).css('animation', 'slide-out 1s ease forwards');

        // Reset animation for next cycle
        setTimeout(() => {
            $texts.eq((current - 1 + numberDivs) % numberDivs).css('animation', '');
        }, 1000); // Match the duration of slide-out
    }

    setInterval(showNextText, 4000); // Change every 3000 ms
});


